     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, Like $like)
    {
        //
        $image =($request->image);
        $user_id=($request->user_id);
        $likes = Like::where('image',$image)->where('user_id',$user_id)->get();

        if(count($likes)>0){
            foreach($likes as $like){
                $like->delete();
                return redirect()->route('home')->with('error','Eliminado Like');
            }
        }else{
            $input = $request->all();
            Like::create($input);
            return redirect()->route('home')->with('success','like.!!');
        }
    }

